// Event listener for department select
departmentSelect.addEventListener("change", () => {
    currentDepartment = departmentSelect.value;
    localStorage.setItem('currentDepartment', currentDepartment);
    employees = employeesData[currentDepartment] || [];
    renderEmployees();

    // Update report table if it's currently displayed
    const reportTable = document.getElementById("reportTable");
    if (reportTable.style.display === 'table') {
        const thead = reportTable.querySelector("thead");
        const tbody = reportTable.querySelector("tbody");
        thead.innerHTML = "";
        tbody.innerHTML = "";

        const month = selectedMonth.getMonth();
        const year = selectedMonth.getFullYear();
        const daysInMonth = new Date(year, month + 1, 0).getDate();

        const dayNames = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];

        // Get valid days (exclude Sundays)
        const validDays = [];
        for (let day = 1; day <= daysInMonth; day++) {
            const date = new Date(year, month, day);
            if (date.getDay() !== 0) { // Not Sunday
                validDays.push({ day, dayName: dayNames[date.getDay()] });
            }
        }

        const statusAbbrev = {
            present: "1",
            absent: "0",
            permission: "P"
        };

        // Create header row
        const headerRow = document.createElement("tr");
        const monthName = selectedMonth.toLocaleString('default', { month: 'short' });
        headerRow.innerHTML = "<th>Employee</th>" + validDays.map(d => `<th>${d.day} ${monthName} (${d.dayName})</th>`).join("") + "<th>Attendance Rate</th>";
        thead.appendChild(headerRow);

        // Create employee rows
        employees.forEach(emp => {
            const tr = document.createElement("tr");
            let rowHtml = `<td>${emp}</td>`;

            let presentDays = 0;
            validDays.forEach(({ day }) => {
                const date = new Date(year, month, day);
                const dateKey = date.toISOString().split('T')[0];
                const data = JSON.parse(localStorage.getItem(dateKey)) || {};
                const status = data[emp] ? statusAbbrev[data[emp]] : "-";
                if (status === "1") presentDays++;
                rowHtml += `<td>${status}</td>`;
            });

            // Calculate attendance rate
            const attendanceRate = validDays.length > 0 ? ((presentDays / validDays.length) * 100).toFixed(1) : "0.0";
            rowHtml += `<td>${attendanceRate}%</td>`;

            tr.innerHTML = rowHtml;
            tbody.appendChild(tr);
        });
    }
});

