// Save employees to localStorage
function saveEmployees() {
    employeesData[currentDepartment] = employees;
    localStorage.setItem('employeesData', JSON.stringify(employeesData));
}

// Render employee rows
function renderEmployees() {
    table.innerHTML = "";

    employees.forEach((name, index) => {
        const tr = document.createElement("tr");

        const nameTd = document.createElement("td");
        nameTd.textContent = name;

        const actionTd = document.createElement("td");
        const actionDiv = document.createElement('div');
        actionDiv.style.display = 'flex';
        actionDiv.style.flexWrap = 'nowrap';
        actionDiv.style.justifyContent = 'center';
        actionDiv.style.gap = '2px';

        ["present", "absent", "permission"].forEach(status => {
            const btn = document.createElement("button");
            btn.textContent = status.toUpperCase();
            btn.className = status;
            btn.style.fontSize="10px";
            btn.style.padding="6px";

            if (attendanceData[name] === status) {
                btn.classList.add("active");
            }

            btn.onclick = () => {
                if (attendanceData[name] === status) {
                    delete attendanceData[name]; // Clear the status if it's already set
                } else {
                    attendanceData[name] = status;
                }
                saveAttendance();
                renderEmployees();
            };

            actionDiv.appendChild(btn);
        });

        actionTd.appendChild(actionDiv);

        const actionsTd = document.createElement("td");
        const removeBtn = document.createElement("button");
        removeBtn.textContent = "Remove";
        removeBtn.className = "remove";
        removeBtn.style.background="green";
        removeBtn.style.padding="6px";
        removeBtn.onclick = () => {
            if (confirm(`Are you sure you want to remove ${name}?`)) {
                employees.splice(index, 1);
                saveEmployees();
                renderEmployees();
            }
        };
        actionsTd.appendChild(removeBtn);

        tr.appendChild(nameTd);
        tr.appendChild(actionTd);
        tr.appendChild(actionsTd);
        table.appendChild(tr);
    });
}

