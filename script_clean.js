// Search filter
searchInput.addEventListener("keyup", () => {
    const filter = searchInput.value.toLowerCase();
    if (filter === "") {
        renderEmployees();
        return;
    }
    // First, try to filter current department
    let visibleRows = 0;
    const rows = Array.from(table.getElementsByTagName("tr")).slice(1);
    rows.forEach(row => {
        const name = row.cells[0].textContent.toLowerCase();
        const display = name.includes(filter) ? "" : "none";
        row.style.display = display;
        if (display === "") visibleRows++;
    });
    if (visibleRows === 0) {
        // Search other departments
        for (const dept in employeesData) {
            if (dept === currentDepartment) continue;
            const emps = employeesData[dept];
            const match = emps.find(emp => emp.toLowerCase().includes(filter));
            if (match) {
                // Switch to this department
                currentDepartment = dept;
                departmentSelect.value = dept;
                localStorage.setItem('currentDepartment', currentDepartment);
                employees = employeesData[currentDepartment] || [];
                renderEmployees();
                // Now filter again
                const newRows = Array.from(table.getElementsByTagName("tr")).slice(1);
                newRows.forEach(row => {
                    const name = row.cells[0].textContent.toLowerCase();
                    row.style.display = name.includes(filter) ? "" : "none";
                });
                break;
            }
        }
    }
});

