// Download PDF
const downloadPdfBtn = document.getElementById("downloadPdfBtn");
downloadPdfBtn.addEventListener("click", () => {
    const month = selectedMonth.getMonth();
    const year = selectedMonth.getFullYear();
    const daysInMonth = new Date(year, month + 1, 0).getDate();

    const dayNames = ['Sun', 'Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat'];

    // Get valid days (exclude Sundays)
    const validDays = [];
    for (let day = 1; day <= daysInMonth; day++) {
        const date = new Date(year, month, day);
        if (date.getDay() !== 0) { // Not Sunday
            validDays.push({ day, dayName: dayNames[date.getDay()] });
        }
    }

    const statusAbbrev = {
        present: "1",
        absent: "0",
        permission: "P"
    };

    const { jsPDF } = window.jspdf;
    const doc = new jsPDF({ orientation: 'landscape' });

    // Prepare table data
    const headers = ['Employee'];
    const monthName = selectedMonth.toLocaleString('default', { month: 'short' });
    validDays.forEach(({ day, dayName }) => {
        headers.push(`${day} ${monthName}\n(${dayName})`);
    });
    headers.push('Attendance Rate');

    const tableData = [];
    let totalAttendanceRate = 0;
    employees.forEach(emp => {
        const row = [emp];
        let presentDays = 0;

        validDays.forEach(({ day }) => {
            const date = new Date(year, month, day);
            const dateKey = date.toISOString().split('T')[0];
            const data = JSON.parse(localStorage.getItem(dateKey)) || {};
            const status = data[emp] ? statusAbbrev[data[emp]] : '-';
            if (status === '1') presentDays++;
            row.push(status);
        });

        // Calculate attendance rate
        const attendanceRate = validDays.length > 0 ? (presentDays / validDays.length) * 100 : 0;
        totalAttendanceRate += attendanceRate;
        const attendanceRateStr = attendanceRate.toFixed(1) + '%';
        row.push(attendanceRateStr);

        tableData.push(row);
    });

    // Calculate department average attendance rate
    const departmentAvgRate = employees.length > 0 ? (totalAttendanceRate / employees.length).toFixed(1) + '%' : '0.0%';

    // Title
    doc.setFontSize(14);
    doc.text(currentDepartment + ' Department - Monthly Attendance Report - ' + selectedMonth.toLocaleString('default', { month: 'long', year: 'numeric' }), 10, 15);
    doc.setFontSize(12);
    doc.text('Department Attendance Rate: ' + departmentAvgRate, 10, 25);

    // Column styles for responsiveness
    const columnStyles = {};
    columnStyles[0] = { cellWidth: 25, fontSize: 10 }; // Employee column larger font
    for (let i = 1; i < headers.length - 1; i++) {
        columnStyles[i] = { cellWidth: 10 }; // Day columns
    }
    columnStyles[headers.length - 1] = { cellWidth: 15 }; // Attendance rate column

    doc.autoTable({
        head: [headers],
        body: tableData,
        startY: 35,
        styles: { fontSize: 7, cellPadding: 1, overflow: 'linebreak' },
        headStyles: { fillColor: [41, 128, 185], fontSize: 9 },
        columnStyles: columnStyles,
        margin: { top: 35, left: 5, right: 5 },
        tableWidth: 'auto',
        pageBreak: 'auto',
        showHead: 'everyPage'
    });

    doc.save('monthly-attendance-report.pdf');
});

